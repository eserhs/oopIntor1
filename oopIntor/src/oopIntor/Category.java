package oopIntor;



public class Category {
	 private int id ;
	private String name ;
	
	public Category(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getid () {
		return this.id;
		
}
	
	public void setid () {
		
		this.id = id;
	}
	
	
	
	public String getname() {
		
		
		return this.name;
		
	
	}
	public void setname (String name) {
		this.name = name;
	}
	
	
	
}
